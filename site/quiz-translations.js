function translateQuizText(text, lang) {
  if (lang === 'en' || !text) return text;

  // Check manual translations first
  if (typeof QUIZ_CONTENT_TRANSLATIONS !== 'undefined' && QUIZ_CONTENT_TRANSLATIONS.manual && QUIZ_CONTENT_TRANSLATIONS.manual[text]) {
    return QUIZ_CONTENT_TRANSLATIONS.manual[text];
  }

  // Try pattern matching
  if (typeof QUIZ_CONTENT_TRANSLATIONS !== 'undefined' && QUIZ_CONTENT_TRANSLATIONS.patterns) {
    for (const [pattern, replacer] of QUIZ_CONTENT_TRANSLATIONS.patterns) {
      if (pattern.test(text)) {
        return text.replace(pattern, replacer);
      }
    }
  }

  // Phrase-level translation (longest match first)
  let result = text;
  const phrases = typeof QUIZ_CONTENT_TRANSLATIONS !== 'undefined' ? QUIZ_CONTENT_TRANSLATIONS.phrases : {};
  const sortedPhrases = Object.entries(phrases).sort((a, b) => b[0].length - a[0].length);

  for (const [en, pt] of sortedPhrases) {
    const regex = new RegExp(en.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
    result = result.replace(regex, pt);
  }

  return result;
}

/**
 * Translate a full quiz question object.
 * @param {Object} qObj - { q, opts, explain, correct }
 * @param {string} lang - Target language
 * @returns {Object} Translated question object
 */
function translateQuizQuestion(qObj, lang) {
  if (lang === 'en' || !qObj) return qObj;

  // Check if we have a manual translation for this question
  const manualKey = qObj.q;
  if (QUIZ_CONTENT_TRANSLATIONS.manual[manualKey]) {
    const translated = QUIZ_CONTENT_TRANSLATIONS.manual[manualKey];
    return {
      q: translated.q || qObj.q,
      opts: translated.opts || qObj.opts,
      explain: translated.explain || qObj.explain,
      correct: qObj.correct
    };
  }

  // Auto-translate using dictionary
  return {
    q: translateQuizText(qObj.q, lang),
    opts: qObj.opts.map(opt => translateQuizText(opt, lang)),
    explain: translateQuizText(qObj.explain, lang),
    correct: qObj.correct
  };
}
